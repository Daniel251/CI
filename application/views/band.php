            <h1>ZESPÓŁ</h1>
            <p><b>NOCNY KOCHANEK</b> to jeden z nielicznych polskich zespołów metalowych, który muzykę,
            a przede wszystkim towarzyszące jej teksty tworzy z ogromnym dystansem do samych
            członków grupy oraz do wizerunku zazwyczaj kojarzonego z heavy metalem.</p>
            <p>Zespół Nocny Kochanek tworzą muzycy grupy Night Mistress. Nazwę zespołu Nocny
            Kochanek zawdzięcza Bartoszowi Walaszkowi – reżyserowi popularnej kreskówki “Kapitan Bomba”,
            w której wielokrotnie można było usłyszeć muzykę Night Mistress. W jednym z odcinków zespół
            wystąpił pod szyldem Nocny Kochanek, będącym prześmiewczym tłumaczeniem nazwy Night Mistress.</p>
           <p> To wydarzenie uznawane jest za datę narodzin Nocnego Kochanka.</p>
            W roku 2012 zespół nagrał utwór do pełnometrażowego filmu o przygodach Kapitana Bomby
            “Zemsta Faraona”. Jest on zatytułowany Minerał Fiutta i zdecydowanie jest to kompozycja
            z przymrużeniem oka.
            <p>Muzykę Nocnego Kochanka należy określić mianem tradycyjnego heavy metalu typowego
            dla takich zespołów jak Iron Maiden, Judas Priest, Black Sabbath czy Saxon.<p>
            <p>Grupa doskonale czuje się na scenie i znakomicie sprawdza na koncertach. W roku 2014 zespół
            wystąpił na XX Przystanku Woodstock.</p>
            <p>6 listopada 2015 ukazała się płyta Nocnego Kochanka zatytułowana "Hewi Metal".</p>
